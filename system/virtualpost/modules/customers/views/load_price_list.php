<script type="text/javascript">
$(document).ready(function(){
    var account_type = 1;
    $("#pricing_list_detail_by_location").click(function(){
        $('#PriceListDetailDiv').openDialog({
            autoOpen: false,
            height: 550,
            width: 1150,
            modal: true,
            closeOnEscape: false,
            open: function(event, ui) {
                $(this).load("<?php echo base_url() ?>customers/view_pricing", function() {
                });
            },
            buttons: {
                'Close': function () {
                    $(this).dialog('destroy');
                }
            }
        });

        $('#PriceListDetailDiv').dialog('option', 'position', 'center');
        $('#PriceListDetailDiv').dialog('open');
        return false;
    });

    $("#pricing_list_detail_asyougo").click(function(){
        account_type = 1;
        loadPriceListDetailWindow($(this).data('type'));

        return false;
    });

    $("#pricing_list_detail_private").click(function(){
        account_type = 2;
        loadPriceListDetailWindow($(this).data('type'));

        return false;
    });

    $("#pricing_list_detail_business").click(function(){
        account_type = 3;
        loadPriceListDetailWindow($(this).data('type'));

        return false;
    });

    $("#pricing_list_detail_enterprise").click(function(){
        account_type = 5;
        loadPriceListDetailWindow($(this).data('type'));

        return false;
    });

    $("#location_id").live('change', function(){
        var location_id = $("#location_id").val();
        $.pageBlock();
        $("#priceListDetailDivContainer").load("<?php echo base_url() ?>customers/load_price_list_detail?hide_flag=1&location_id="+location_id+"&type="+account_type, function() {
            $.pageUnblock();
        });
    });

    function loadPriceListDetailWindow(type){
        $("#PriceListDetailDiv").html("");

        var location_id = $("#location_id").val();

        if(!location_id){
            location_id = 1;
        }

        // Open new dialog
        $('#PriceListDetailDiv').openDialog({
            autoOpen: false,
            height: 550,
            width: 1200,
            modal: true,
            closeOnEscape: false,
            open: function(event, ui) {
                $(this).load("<?php echo base_url() ?>customers/load_price_list_detail?location_id="+location_id+"&type="+account_type, function() {
                });
            }
        });

        $('#PriceListDetailDiv').dialog('option', 'position', 'center');
        $('#PriceListDetailDiv').dialog('open');
    }
    
    $('.select-postbox-btn').die('click');
    $('.select-postbox-btn').live('click', function(){
        var type = $(this).data('type');
        $('#saveRegisterPostboxNameFormPriceList').dialog('destroy');
        $("#saveRegisterPostboxNameForm_account_type").val(type);
    });
    
    $('#switchEnterpriseButton').die('click');
    $('#switchEnterpriseButton').live('click', function(){
        // Call method to upgrade to enterprise
        upgradeEnterpriseCustomer();
    });
    
    function upgradeEnterpriseCustomer() {
        var message = $("#upgradeEnterpriseCustomerConfirmDivContainer").html();
        // Show confirm dialog
        $.confirm({
            message: message,
            yes: function () {
                openUpgradeEnterpriseCustomerConfirm();
            }
        });
        return false;
    }
    
    // Open form to confirm
    function openUpgradeEnterpriseCustomerConfirm() {
        console.log('Start openUpgradeEnterpriseCustomerConfirm');
        // Clear control of all dialog form
        // $('#upgradeEnterpriseCustomerConfirmWindow').html('');
        // Open new dialog
        $('#setupProcessUpgradeEnterpriseCustomerConfirmWindow').openDialog({
            autoOpen: false,
            height: 550,
            width: 700,
            modal: true,
            open: function () {
                $(this).load("<?php echo base_url() ?>account/upgrade_customer_type?setup_flag=1", function () {
                });
            }
        });
        $('#setupProcessUpgradeEnterpriseCustomerConfirmWindow').dialog('option', 'position', 'center');
        $('#setupProcessUpgradeEnterpriseCustomerConfirmWindow').dialog('open');
        console.log('End openUpgradeEnterpriseCustomerConfirm');
    }
});
</script>
